function SpriteCanvas({id, scale=3}) {
  const ref = useRef(null);
  const spr = SPRITES[id];
  useEffect(()=>{
    if(!ref.current||!spr) return;
    const ctx = ref.current.getContext("2d");
    ctx.fillStyle = P.N1;
    ctx.fillRect(0,0,spr.w*scale,spr.h*scale);
    for(const [x,y,c] of spr.pixels){
      if(!c||c===P._) continue;
      ctx.fillStyle = c;
      ctx.fillRect(x*scale, y*scale, scale, scale);
    }
  },[id,scale,spr]);
  if(!spr) return <div style={{color:"#f44",fontSize:9}}>{id}</div>;
  return <canvas ref={ref} width={spr.w*scale} height={spr.h*scale} style={{display:"block",imageRendering:"pixelated"}}/>;
}

